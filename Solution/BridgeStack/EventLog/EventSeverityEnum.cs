namespace BridgeStack
{
	/// <summary>
	/// Event message severity enumeration.
	/// </summary>
	public enum EventSeverityEnum
	{
		/// <summary>
		/// A diagnostic message.
		/// </summary>
		Debug,
		/// <summary>
		/// A nice-to-have message.
		/// </summary>
		Info,
		/// <summary>
		/// A warning message.
		/// </summary>
		Warning,
		/// <summary>
		/// An error message.
		/// </summary>
		Error,
		/// <summary>
		/// A fatal error message.
		/// </summary>
		Fatal
	}
}